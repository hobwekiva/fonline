int CMapMngr::Init()
{
	if(active) return 1;

	if(!fm.Init()) return 0;


	active=TRUE;
	return 1;
}

void CMapMngr::Clear()
{
	for(int i=0; i<MAX_MAPS; i++)
		transit_map[i].clear();

	active=FALSE;
}




